# List
list=['Apple','Orange','Jerry','Banana'] 
print("This is our list ",list) 
i=1 
for val in list: 
    print("List Item",i,"is",val)

# Tuple
tuple=('Apple','Orange','Jerry','Banana')
print("This is our tuple ",tuple)
i=1
for val in tuple:
    print("Tuple Item",i,"is",val)

# String
